void pressButton(String button_name)
{
    Serial.print(button_name);
    Serial.println(" button pressed");
    delay(10);
    display.setCursor(0, 0);
    display.print(button_name);
    display.println(" button pressed");
    delay(10);
    display.display();
    delay(10);
    display.clearDisplay();
    display.display();
    delay(10);
}

